/**
 * Look up circuit data by circuit_short_name or similar key.
 * Falls back to a fuzzy match.
 */
export function getCircuitData(circuitName) {
  if (!circuitName) return null;
  // Exact match
  if (CIRCUITS[circuitName]) return CIRCUITS[circuitName];
  // Try partial matching
  const lower = circuitName.toLowerCase();
  for (const [key, data] of Object.entries(CIRCUITS)) {
    if (lower.includes(key.toLowerCase()) || key.toLowerCase().includes(lower)) {
      return data;
    }
  }
  // Try matching by fullName
  for (const data of Object.values(CIRCUITS)) {
    if (data.fullName.toLowerCase().includes(lower) || lower.includes(data.fullName.toLowerCase())) {
      return data;
    }
  }
  return null;
}

export default CIRCUITS;
